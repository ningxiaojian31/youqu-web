<template>
	<view>
		<view id="main">
			<view class="pd-10 bg-fff">

				<template v-if="list.length>0">
					<view v-for="(item,index) in list" :key="time+'.'+item.id">
						<view class="chatbox" v-if="item.userid==1">
							<view class="flex-1" :a="index"></view>
							<view class="chatbox-desc-b mgb-5 mgr-5">
								<chat-msg :content="item.message"></chat-msg>
							</view>
							
							<image :src="item.user_head+'.100x100.jpg'" class="wh-40 mgr-10 bd-radius-10"></image>
						</view>
						<view class="chatbox" v-else>
							<image :src="item.user_head+'.100x100.jpg'" class="wh-40 mgr-10 bd-radius-10"></image>
							<view class="flex-1">
								<view class="chatbox-nick-a mgb-5">{{item.userid}}</view>
								<view class="chatbox-desc-a">
									<chat-msg :content="item.message"></chat-msg>
								</view>
							</view>
						</view>

					</view>
				</template>
				<template v-else>
					<view class="emptyData">暂无消息</view>
				</template>

			</view>
			<view class="fixFoot-row"></view>
			<view class="fixFoot bg-fff pdb-5">
				<view class="input-flex">
					<input class="input-flex-text" v-model="content" type="text">
					<view class="input-flex-btn w60" @click="send('content')">发送</view>
				</view>
				<view class="flex flex-center">
					<!-- #ifndef H5 -->
					<view @click="aRecordClass='flex-col'" class="flex-1 iconfont icon-voicefill f20"></view>
					<!-- #endif -->
					<view @click="choiceImg('pic')" class="flex-1 iconfont icon-pic f20 sendPic"></view>
					<!-- #ifndef H5 -->
					<view @click="catchImg('pic')" class="flex-1 iconfont icon-camera f20 sendPic"></view>
					<!-- #endif -->
					<!-- #ifndef H5 -->
					<view @click="videoRecord('video')" class="flex-1 iconfont icon-recordlight f20"></view>
					<!-- #endif -->
					<view @click="emoClass='flex-col'" class="flex-1 iconfont icon-emoji f20"></view>
					<view @click="choiceFile('file')" class="flex-1 iconfont none icon-file f20 "></view>
				</view>

			</view>
			<view id="emoModal" :class="emoClass" class="modal-group">
				<view class="modal-mask" @click="emoClass=''"></view>
				<view class="emoFixbox">

					<view class="pd-10">
						<view class="flex flex-wrap">
							<view @click="addEmo(item)" class="imEmo" :class="'imEmo-'+index" v-for="(item,index) in emoList" :key="index"></view>
						</view>
					</view>
				</view>
			</view>

			<view class="modal-group" :class="aRecordClass">
				<view class="modal-mask" @click="aRecordClass=''"></view>

				<view @click="aRecordToggle" class="aRecordBox">
					<view :class="{'aRecord-active':aRecordIng}" class="iconfont icon-voice f36"></view>
					<view v-if="aRecordIng">点击结束</view>
					<view v-else>点击录音</view>
				</view>

			</view>


		</view>
	</view>
</template>

<script>
	import chatDb from "@/common/chatdb.js"
	import chatMsg from "@/components/chatmsg.vue";
	import emo from "@/common/emo.js";
	import common from "@/common/common.js";
	var gid;
	var groupid;
	var uid = Date.parse(new Date());
	var touid = ""
	var ws;
	var inPage = false;
	var windowHeight=0;
	var lastMsg;
	var audioRecord;
	var inAjax=false;
	const aRecordOptions = {
		duration: 10000,
		sampleRate: 44100,
		numberOfChannels: 1,
		encodeBitRate: 192000,
		format: 'mp3',
		frameSize: 50
	}
	export default {
		components: {
			chatMsg
		},
		data: function() {
			return {
				per_page: 0,
				"list": [],
				content: "",
				wsConn: false,
				group: {},
				user: {},
				toUser: {},
				emoList: [],
				sch: 0,
				oldsch: 0,
				scrollTop: 10000,
				emoClass: "",
				aRecordClass: "",
				aRecordIng: false,
				time:0
			}
		},
		onPageScroll:function(e){
			if(e.scrollTop==0 && !inAjax){
				this.getList();
				inAjax=true;
				setTimeout(function(){
					inAjax=false;
				},2000);
			}
		},
		onLoad: function() {
			
			this.initList();
			
			
			var sys=uni.getSystemInfoSync()
			windowHeight=sys.windowHeight;	
			var that = this;
			this.emoList = emo.emoList();
			groupid=ops.groupid;
			this.getPage();
		
		
			//#ifndef H5
			audioRecord = wx.getRecorderManager();


			audioRecord.onStop((res) => {
				if (res.tempFilePath) {
					that.recordUpload(res.tempFilePath);
				}

			})

			audioRecord.onError((res) => {
				console.log(e)
			})
			//#endif

		},
		onHide: function() {
			if (inPage) {
				return false;
			}
			ws.close({
				success: function(res) {

				}
			});
		},
		onShow: function() {
			if (inPage) {
				return false;
			}
			var that = this;
			if (this.wsConn) {
				that.wsInit();
			}
			
		},
		methods: {
			scrollY: function(e) {
				this.sch = e.detail.scrollHeight;
			},
			scTop: function(e) {
				if (this.oldsch == 0) {
					this.oldsch = this.sch;
				}

				var that = this;
				that.scrollTop = 0;
				this.getList();
			},
			//加载聊天记录
			initList:function(){
				var url = common.apiHost+'/chatrecord/findByUserIdAndFriendId?userid=1&friendid=1053624336767909888';
				var method = "GET";
				common.request(url,null,method).then(data => {
					console.log("加载当前聊天=======");
				    //console.log(JSON.stringify(data));
				  this.list = data.data;
				})
			},
			
			getList: function() {
				this.time=Date.parse(new Date())/1000;
				var that = this;
				if (this.per_page == 0) return false;
				that.app.get({
					url: that.app.apiHost + "/chatrecord/findByUserIdAndFriendId?userid=1&friendid=1053624336767909888",
					data: {
						per_page: that.per_page
					},
					success: function(res) {
						that.per_page = res.data.per_page;
						var list = that.list;
						for (var i in res.data.list) {
							list.unshift(res.data.list[i]);
						}						
						//that.list =list;
						that.list = res.data;
						console.log(JSON.stringify(res.data));
						setTimeout(function() {
							that.scrollTop += 10;
						}, 100)
						setTimeout(function(){
							const query = uni.createSelectorQuery().in(that);
							 query.select('#main').boundingClientRect(res => {
								if(that.oldsch==0){
									uni.pageScrollTo({
										scrollTop:100000,
										duration:1
									})
								}else{
									var st=res.height-that.oldsch-windowHeight+160;
									uni.pageScrollTo({
										scrollTop:st,
										duration:1
									})
								}
								that.oldsch=res.height;
							}).exec();
						},10)
					}
				})
			},
			getPage: function() {
				var that = this;

				that.app.get({
					url: that.app.apiHost + "/module.php?m=im_group&a=home&ajax=1&groupid=" + groupid,
					success: function(res) {
						gid = res.data.ws_gid;
						that.per_page = res.data.per_page;
						that.group = res.data.group;
						that.user=res.data.user;
						uni.setNavigationBarTitle({
							title: res.data.group.title
						})
						/*
						var list=chatDb.msgList({
							gid:gid
						});
						*/
						that.list = res.data.list;
						that.wsInit();
						setTimeout(function() {
							that.wsConn = true;
						}, 1000)
						var it=setTimeout(function(){
							const query = uni.createSelectorQuery().in(that);
							query.select('#main').boundingClientRect(function(res) {				  
								that.oldsch=res.height;
							}).exec();
							uni.pageScrollTo({
								scrollTop:that.scrollTop+10,
								duration:1
							})
							
						},30)
					}
				})
			},
			wsInit: function() {
				var that = this;

				if (this.wsConn) {
					ws.close();
				}
				ws = uni.connectSocket({
					url: that.app.wsHost,
					complete: function(res) {

					}
				});

				ws.onOpen(function(res) {

					var msg = JSON.stringify({
						type: "login",
						k: uid,
						gid: gid,
						appGroupId: that.group.groupid,
						content: "login"
					});
					ws.send({
						data: msg
					});
				});
				ws.onError(function(res) {


				});
				ws.onMessage(function(e) {
					var res = JSON.parse(e.data);
					console.log(res);
					switch (res.type) {
						case "login":
							break;
						case "say":
							var json = {
								gid: gid,
								imgurl: that.group.imgurl,
								appGroupId: that.group.groupid,
								appGroupTitle: that.group.title,
								uid: res.wsclient_from,
								touid: res.wsclient_to,
								content: res.content,
								time: res.time,
								isme: uid == res.wsclient_from ? true : false
							}
								
							that.addMsg(json);
							//chatDb.addGroup(json);
							setTimeout(function() {
								uni.pageScrollTo({
									scrollTop: 1000000
								});
							}, 100)
							break;
					}
				});
			},
			addMsg: function($msg) {
				var list = this.list;
				list.push($msg);
				this.list = list;

			},
			addEmo: function(s) {
				s = "\\" + s + " ";
				this.content += s;
			},
			send: function(type, fileurl) {
				var that = this;
				var content;
				switch (type) {
					case "pic":
						content = "[img=" + fileurl + "]"
						break;
					case "audio":
						content = "[audio=" + fileurl + "]"
						break;
					case "video":
						content = "[video=" + fileurl + "]"
						break;
					case "file":
						content = fileurl.substring(fileurl.lastIndexOf("\/") + 1) + " [file=" + fileurl + "]";
						break;
					case "content":
						content = that.content;
						if (content == "") {
							return false;
						}
						break;
				}
				var msg = common.getMessage(common.MSG_TYPE_SEND, 1, 1, content, null, null);
                console.log(msg);
				lastMsg = msg;
		        //发送消息
				Vue.prototype.socketTask.send({
					data:JSON.stringify(msg),
					success(){
						console.log("发送成功========");
					},
					fail(){
						console.log("发送失败========");
					}
				});
				//that.saveHost(content);
				that.content = "";

			},
			saveHost: function(content) {
				var that = this;
				that.app.post({
					url: that.app.apiHost + "/module.php?m=im_group_msg&a=save",
					data: {
						groupid: that.group.groupid,
						content: content
					},
					success: function(res) {
						console.log(res)
					}

				})
			},
			choiceImg: function() {
				//选择图片发送
				var that = this;
				inPage = true;
				uni.chooseImage({
					sourceType: ["album"],
					fail: function(e) {
						inPage = false;
					},
					success: function(e) {
						inPage = false;
						uni.uploadFile({
							url: that.app.apiHost + "/index.php?m=upload&a=img&ajax=1&authcode=" + that.app.getAuthCode(), //仅为示例，非真实的接口地址
							filePath: e.tempFilePaths[0],
							name: 'upimg',
							dataType: "json",

							success: (res) => {
								if (!res.data.error) {
									var rs = JSON.parse(res.data);
									if (!rs.error) {
										that.send("pic", rs.data.trueimgurl);
									}
								}
							}
						});
					}
				})
			},
			choiceFile: function() {
				//选择图片发送
				var that = this;
				inPage = true;
				uni.chooseImage({
					fail: function(e) {
						inPage = false;
					},
					success: function(e) {
						inPage = false;
						uni.uploadFile({
							url: that.app.apiHost + "/index.php?m=upload&a=upload&ajax=1&authcode=" + that.app.getAuthCode(), //仅为示例，非真实的接口地址
							filePath: e.tempFilePaths[0],
							name: 'upimg',
							dataType: "json",

							success: (res) => {
								if (!res.error) {
									var rs = JSON.parse(res.data);
									if (!rs.error) {
										that.send("file", rs.trueimgurl);
									}
								}
							}
						});
					}
				})
			},
			catchImg: function() {
				var that = this;
				inPage = true;
				uni.chooseImage({
					fail: function(e) {
						inPage = false;
					},
					sourceType: ["camera"],
					success: function(e) {
						inPage = false;
						uni.uploadFile({
							url: that.app.apiHost + "/index.php?m=upload&a=img&ajax=1&authcode=" + that.app.getAuthCode(), //仅为示例，非真实的接口地址
							filePath: e.tempFilePaths[0],
							name: 'upimg',
							dataType: "json",

							success: (res) => {
								if (!res.data.error) {
									var rs = JSON.parse(res.data);
									if (!rs.data.error) {
										that.send("pic", rs.data.trueimgurl);
									}
								}
							}
						});
					}
				})
			},
			videoRecord: function() {
				var that = this;
				uni.chooseVideo({
					count: 1,
					success: function(e) {
						uni.uploadFile({
							url: that.app.apiHost + "/index.php?m=upload&a=uploadmp4&ajax=1&authcode=" + that.app.getAuthCode(),
							filePath: e.tempFilePath,
							name: 'upimg',
							dataType: "json",

							success: (res) => {
								if (!res.data.error) {
									var rs = JSON.parse(res.data);
									if (!rs.error) {
										that.send("video", rs.trueimgurl);
									}
								}
							}
						});
					}
				})
			},
			recordUpload: function(fileurl) {
				var that = this;
				console.log(fileurl);
				uni.uploadFile({
					url: that.app.apiHost + "/index.php?m=upload&a=uploadmp4&ajax=1&authcode=" + that.app.getAuthCode(),
					filePath: fileurl,
					fileType: "audio",
					name: 'upimg',
					dataType: "json",

					success: (res) => {
						if (!res.data.error) {
							var rs = JSON.parse(res.data);
							if (!rs.error) {
								that.send("audio", rs.trueimgurl);
							}
						}
					}
				});
			},
			aRecordToggle: function() {
				if (this.aRecordIng) {
					console.log("stop")
					audioRecord.stop();
					this.aRecordIng = false;
				} else {
					this.aRecordIng = true;

					audioRecord.start(aRecordOptions);
				}


			}
		}
	}
</script>

